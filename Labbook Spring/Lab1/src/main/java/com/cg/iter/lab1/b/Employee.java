package com.cg.iter.lab1.b;

public class Employee {
	private int empId;
	private String empName;
	private double salary;
	private SBU buUnit;
	private int age;
	public int getEmpId() {
		return empId;
	}
	public void setEmplId(int empId) {
		this.empId = empId;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmplName(String empName) {
		this.empName = empName;
	}
	public double getSalary() {
		return salary;
	}
	public void setSalary(double salary) {
		this.salary = salary;
	}
	public SBU getBuUnit() {
		return buUnit;
	}
	public void setBuUnit(SBU buUnit) {
		this.buUnit = buUnit;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
}
